// VARIABLES AND CONSTANTS

// The value of this greeting variable can change over time, as it is defined using the var keyword
// The String "Hello, playground", will be the initial value of the greeting variable
var greeting = "Hello, playground"
var name = "Ted"
// The value of the name variable is reassigned or changed multiple times below.
// The var keyword does not need to be written over and over again when we want to change the value of a variable
name = "Rebecca"
name = "Keeley"

// The value of this character constant cannot be changed over time, as it is defined using the let keyword
// We make use of constants to store values that we do not want to change later in the program
let character = "Daphne"

// we word our variable playerName using the camelCase naming convention.
// It is an industry standard among Swift developers
var playerName = "Roy"
// We make use of the print() function to see the value of a variable in our shell / debugging area
print(playerName)

playerName = "Dani"
print(playerName)

playerName = "Sam"
print(playerName)

let managerName = "Michael Scott"
let dogBreed = "Samoyed"
let meaningOfLife = "How many roads must a man walk down?"